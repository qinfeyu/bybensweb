      function getFiltered() {
        let list = [...allProducts];
        const q = state.search.toLowerCase().trim();
        if (q)
          list = list.filter(
            (p) =>
              p.name.toLowerCase().includes(q) ||
              (p.brand || "").toLowerCase().includes(q) ||
              (p.description || "").toLowerCase().includes(q) ||
              (p.flavors || []).some((f) => f.name.toLowerCase().includes(q)),
          );
        if (state.categories.length)
          list = list.filter((p) =>
            (p.categoryIds || []).some((id) => state.categories.includes(id)),
          );
        if (state.subCategories.length)
          list = list.filter((p) =>
            state.subCategories.some((subId) => {
              return p.subCategoryIds.includes(subId);
            }),
          );
        if (state.brands.length)
          list = list.filter((p) => state.brands.includes(p.brand));
        if (state.priceMin !== "")
          list = list.filter(
            (p) => getProductPrice(p) >= Number(state.priceMin),
          );
        if (state.priceMax !== "")
          list = list.filter(
            (p) => getProductPrice(p) <= Number(state.priceMax),
          );
        if (state.availability === "in-stock") {
          list = list.filter((p) => Number(p.stock) > 0);
        } else if (state.availability === "out-of-stock") {
          list = list.filter((p) => Number(p.stock) <= 0);
        }

        switch (state.sort) {
          case "price-asc":
            list.sort((a, b) => getProductPrice(a) - getProductPrice(b));
            break;
          case "price-desc":
            list.sort((a, b) => getProductPrice(b) - getProductPrice(a));
            break;
          case "newest":
            list.sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
            break;
          case "name":
            list.sort((a, b) => a.name.localeCompare(b.name));
            break;
        }
        return list;
      }

