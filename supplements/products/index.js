      function toggleFilter(key, value, el) {
        const arr = state[key];
        const idx = arr.indexOf(value);
        if (idx === -1) arr.push(value);
        else arr.splice(idx, 1);
        el.classList.toggle("checked", arr.includes(value));
        // When a category is deselected and categories remain selected,
        // remove subcategory selections that no longer belong to any active category
        if (key === "categories" && state.categories.length > 0) {
          state.subCategories = state.subCategories.filter((subId) => {
            const subOpt = subCategoryOptions.find((o) => o.value === subId);
            return (
              subOpt &&
              subOpt.categoryIds.some((cid) => state.categories.includes(cid))
            );
          });
        }
        refresh();
      }

